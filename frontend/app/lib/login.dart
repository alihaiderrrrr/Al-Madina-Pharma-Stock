class LoginPage extends StatelessWidget {
  LoginPage({super.key});

  // text editing controllers
  final usernameController = TextEditingController();
  final passwordController = TextEditingController();

  void dispose() {
    usernameController.dispose();
    passwordController.dispose();
  }

  final String Node_url = '$service_url/login';

// Define the signup function

  Future<void> signUserIn(BuildContext context) async {
    String username = usernameController.text;
    String password = passwordController.text;

    // Perform login validation or any other logic with the entered values

    // Clear the text fields after login

    if (username != null && password.length > 1) {
      Map<String, String> body = {'email': username, 'password': password};

      // Send the POST request

      http.Response response = await http.post(Uri.parse(Node_url), body: body);
      print(body);
      if (response.statusCode == 200) {
        print('login successfully');

        Navigator.push(
          context,
          MaterialPageRoute(
            builder: (context) => AnimatedGridAppBar(),
          ),
        );
        usernameController.clear();
        passwordController.clear();
      } else if (response.statusCode == 400) {
        print('Invalid Password');
      } else {
        print('error failed ');
      }
    }
  }

